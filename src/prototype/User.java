package prototype;

public class User {
	
	String identifier;
	String avaiter;
	public User(String identifier) {
		this.identifier = identifier;
	}
	public String getIdentifier() {
		return identifier;
	}
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	public String getAvaiter() {
		return avaiter;
	}
	public void setAvaiter(String avaiter) {
		this.avaiter = avaiter;
	}

}
